        //Display Selected Row In JTextFields
        int i = jTable4.getSelectedRow();
        TableModel model = jTable4.getModel();
        jTextField29.setText(model.getValueAt(i, 0).toString());
        visit.setText(model.getValueAt(i, 1).toString());
        idno.setText(model.getValueAt(i, 2).toString());
        String department = model.getValueAt(i, 3).toString();
        switch (department) {
            case "--Select Department--":
                jComboBox1.setSelectedIndex(0);
                break;
            case "Microbiology":
                jComboBox1.setSelectedIndex(1);
                break;
            case "Neonatal unit":
                jComboBox1.setSelectedIndex(2);
                case "Nephrology":
                jComboBox1.setSelectedIndex(3);
                case "Neurology":
                jComboBox1.setSelectedIndex(4);
        }
        String doctor = model.getValueAt(i, 4).toString();
        switch (doctor) {
            case "--Select Doctor--":
                jComboBox2.setSelectedIndex(0);
                break;
            case "Bhargav":
                jComboBox2.setSelectedIndex(1);
                break;
            case "Bablu":
                jComboBox2.setSelectedIndex(2);
                break;
                case "Rajesh":
                jComboBox2.setSelectedIndex(3);
                break;
                case "Raju":
                jComboBox2.setSelectedIndex(4);
                break;
        }
        pn.setText(model.getValueAt(i, 5).toString());
        fn.setText(model.getValueAt(i, 6).toString());
        jTextField20.setText(model.getValueAt(i, 7).toString());
        String sex = model.getValueAt(i, 8).toString();
        switch (sex) {
            case "--Select Department--":
                jComboBox3.setSelectedIndex(0);
                break;
            case "M":
                jComboBox3.setSelectedIndex(1);
                break;
            case "F":
                jComboBox3.setSelectedIndex(2);
        }
        jTextField22.setText(model.getValueAt(i, 9).toString());
        jTextArea2.setText(model.getValueAt(i, 10).toString());
